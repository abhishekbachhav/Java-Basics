
// Increment operator is classified in two types : Post and Pre increment operator.
// here we gone see Pre increment operator

public class PreIncOP {
    public static void main(String[] args) {
        int a=10;
        int b = ++a; // it first increments the value by 1 then uses the value.thus it Pre(before) increments the value.
        System.out.println(b);
    }
    
}
 