
// Increment operator is classified in two types : Post and Pre increment operator.
// here we gone see Post increment operator

public class PostIncOP {
    public static void main(String[] args) {
        int a=10;
        int b = a++; // it first uses the value and then increments the value by 1.thus it post(after) increments the value.
        System.out.println(b);
    }
    
}
 