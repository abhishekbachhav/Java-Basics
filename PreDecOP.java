
// Decrement operator is classified in two types : Post and Pre decrement operator.
// here we gone see Pre decrement operator

public class PreDecOP {
    public static void main(String[] args) {
        int a=10;
        int b = --a;// it first decrements the value by 1 then uses the value.thus it Pre(before) decrements the value.
        System.out.println(b);
    }
    
}
 