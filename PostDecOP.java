
// Decrement operator is classified in two types : Post and Pre decrement operator.
// here we gone see Post decrement operator

public class PostDecOP {
    public static void main(String[] args) {
        int a=10;
        int b = a--; // it first uses the value and then decrease the value by 1.thus it post(after) decrements the value.
        System.out.println(b);
        System.out.println(b);
    }
    
}
 